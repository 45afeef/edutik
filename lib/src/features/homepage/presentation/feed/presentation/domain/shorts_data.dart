class ShortsData {
  final String creatorProfile;
  final String creatorName;
  final String description;
  final String videoUrl;
  final String title;
  final int likes;
  final int views;

  ShortsData({
    required this.creatorProfile,
    required this.creatorName,
    required this.description,
    required this.videoUrl,
    required this.title,
    required this.likes,
    required this.views,
  });
}
